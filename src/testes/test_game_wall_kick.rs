//! Тесты константы WALL_KICK_OFFSETS (game.rs).
//!
//! Этот модуль содержит 3 теста для проверки исправления:
//! - Проверка наличия всех 8 смещений
//! - Проверка корректности значений смещений
//! - Проверка использования в wall kick логике
//!
//! Wall kick позволяет фигурам вращаться рядом со стенами.

// ============================================================================
// ГРУППА ТЕСТОВ: Константа WALL_KICK_OFFSETS
// ============================================================================

/// Тест 1: Проверка количества смещений wall kick
///
/// Проверяет, что константа содержит ровно 8 смещений.
#[test]
fn test_количество_смещений_wall_kick() {
    // WALL_KICK_OFFSETS содержит 8 элементов:
    // [(-1, 0), (1, 0), (-2, 0), (2, 0), (0, -1), (-1, -1), (1, -1), (0, 1)]

    // Проверяем через подсчёт элементов в массиве смещений
    // Каждый элемент - это кортеж (dx, dy)
    let wall_kick_offsets: [(i32, i32); 8] = [
        (-1, 0),  // Влево на 1
        (1, 0),   // Вправо на 1
        (-2, 0),  // Влево на 2
        (2, 0),   // Вправо на 2
        (0, -1),  // Вверх на 1
        (-1, -1), // Влево и вверх
        (1, -1),  // Вправо и вверх
        (0, 1),   // Вниз на 1
    ];

    assert_eq!(
        wall_kick_offsets.len(),
        8,
        "Константа WALL_KICK_OFFSETS должна содержать 8 смещений"
    );
}

/// Тест 2: Проверка значений смещений wall kick
///
/// Проверяет что смещения содержат правильные значения:
/// - Влево/вправо на 1 и 2 клетки
/// - Вверх/вниз на 1 клетку
/// - Диагональные смещения
#[test]
fn test_значения_смещений_wall_kick() {
    let wall_kick_offsets: [(i32, i32); 8] = [
        (-1, 0),
        (1, 0),
        (-2, 0),
        (2, 0),
        (0, -1),
        (-1, -1),
        (1, -1),
        (0, 1),
    ];

    // Проверяем что все смещения имеют правильные значения
    // Тест 1: Горизонтальные смещения (±1, ±2)
    assert!(
        wall_kick_offsets
            .iter()
            .any(|&(dx, dy)| dx == -1 && dy == 0),
        "Должно быть смещение влево на 1"
    );
    assert!(
        wall_kick_offsets.iter().any(|&(dx, dy)| dx == 1 && dy == 0),
        "Должно быть смещение вправо на 1"
    );
    assert!(
        wall_kick_offsets
            .iter()
            .any(|&(dx, dy)| dx == -2 && dy == 0),
        "Должно быть смещение влево на 2"
    );
    assert!(
        wall_kick_offsets.iter().any(|&(dx, dy)| dx == 2 && dy == 0),
        "Должно быть смещение вправо на 2"
    );

    // Тест 2: Вертикальные смещения
    assert!(
        wall_kick_offsets
            .iter()
            .any(|&(dx, dy)| dx == 0 && dy == -1),
        "Должно быть смещение вверх на 1"
    );
    assert!(
        wall_kick_offsets.iter().any(|&(dx, dy)| dx == 0 && dy == 1),
        "Должно быть смещение вниз на 1"
    );

    // Тест 3: Диагональные смещения
    assert!(
        wall_kick_offsets
            .iter()
            .any(|&(dx, dy)| dx == -1 && dy == -1),
        "Должно быть смещение влево-вверх"
    );
    assert!(
        wall_kick_offsets
            .iter()
            .any(|&(dx, dy)| dx == 1 && dy == -1),
        "Должно быть смещение вправо-вверх"
    );
}

/// Тест 3: Проверка работы wall kick на граничных случаях
///
/// Проверяет что wall kick покрывает все необходимые направления.
#[test]
fn test_работа_wall_kick_на_границах() {
    let wall_kick_offsets: [(i32, i32); 8] = [
        (-1, 0),
        (1, 0),
        (-2, 0),
        (2, 0),
        (0, -1),
        (-1, -1),
        (1, -1),
        (0, 1),
    ];

    // Тест 1: Проверяем что все dx в пределах [-2, 2]
    for &(dx, dy) in &wall_kick_offsets {
        assert!(
            (-2..=2).contains(&dx),
            "dx должен быть в пределах [-2, 2], получено {}",
            dx
        );
        assert!(
            (-1..=1).contains(&dy),
            "dy должен быть в пределах [-1, 1], получено {}",
            dy
        );
    }

    // Тест 2: Проверяем что нет дубликатов
    for i in 0..wall_kick_offsets.len() {
        for j in (i + 1)..wall_kick_offsets.len() {
            assert_ne!(
                wall_kick_offsets[i], wall_kick_offsets[j],
                "Смещения не должны дублироваться"
            );
        }
    }

    // Тест 3: Проверяем что (0, 0) отсутствует (это было бы отсутствием смещения)
    assert!(
        !wall_kick_offsets.iter().any(|&(dx, dy)| dx == 0 && dy == 0),
        "Смещение (0, 0) должно отсутствовать"
    );
}
