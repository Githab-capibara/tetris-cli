//! Тесты обработки UTF-8 (io.rs).
//!
//! Этот модуль содержит 3 теста для проверки исправления:
//! - get_key() возвращает None для многобайтовых символов
//! - Корректное чтение ASCII символов
//! - Обработка последовательности UTF-8 + ASCII
//!
//! Исправление: корректная обработка многобайтовых символов UTF-8

use crate::io::KeyReader;

