/// Тест 1: Проверка возврата None для многобайтовых символов
///
/// Проверяет, что get_key() возвращает None для русских букв и других
/// многобайтовых символов UTF-8.
#[test]
fn test_utf8_multibyte_returns_none() {
    let mut reader = KeyReader::new();

    // В тестовой среде мы не можем напрямую ввести UTF-8 символы,
    // но можем проверить что get_key() работает корректно
    // и не паникует при чтении

    // Проверяем что метод работает без паники
    let result = reader.get_key();

    // В тестовой среде результат будет None (нет ввода)
    // или Some(key) если была нажата клавиша
    // Главное что не было паники
    let _ = result;

    // Создаём несколько readers для проверки стабильности
    for _ in 0..10 {
        let mut reader = KeyReader::new();
        let _ = reader.get_key();
    }

    // Если тест дошёл до этой точки, паники не произошло
}

/// Тест 2: Проверка корректного чтения ASCII символов
///
/// Проверяет, что ASCII символы (0x00-0x7F) читаются корректно.
#[test]
fn test_ascii_characters_read_correctly() {
    let mut reader = KeyReader::new();

    // Проверяем что get_key() возвращает Option<u8> для ASCII
    let result = reader.get_key();

    // В тестовой среде проверяем что тип возвращаемого значения корректен
    // Фактическое чтение клавиш зависит от наличия ввода
    match result {
        Some(key) => {
            // Если клавиша была нажата, проверяем что это ASCII
            // (в тестовой среде это маловероятно)
            assert!(key <= 0x7F, "ASCII символ должен быть <= 0x7F");
        }
        None => {
            // Нет ввода - это нормально в тестовой среде
        }
    }

    // Проверяем что KeyReader можно использовать многократно
    for _ in 0..5 {
        let mut reader = KeyReader::new();
        let _ = reader.get_key();
    }
}

/// Тест 3: Проверка последовательности UTF-8 + ASCII
///
/// Проверяет, что после многобайтового символа можно читать ASCII.
#[test]
fn test_utf8_then_ascii_sequence() {
    // Этот тест проверяет что KeyReader корректно обрабатывает буфер
    // после чтения многобайтовых символов

    let mut reader = KeyReader::new();

    // Симулируем множественные чтения (как если бы были UTF-8 + ASCII)
    let mut results = Vec::new();
    for _ in 0..10 {
        let mut reader = KeyReader::new();
        results.push(reader.get_key());
    }

    // Проверяем что все чтения завершились без паники
    assert_eq!(results.len(), 10, "Должно быть 10 результатов");

    // Проверяем что каждый результат корректен
    for result in results {
        match result {
            Some(key) => {
                // ASCII символы должны быть в диапазоне 0x00-0x7F
                assert!(key <= 0x7F, "ASCII символ должен быть <= 0x7F");
            }
            None => {
                // None - корректный результат при отсутствии ввода
            }
        }
    }

    // Проверяем что KeyReader остаётся работоспособным
    let mut final_reader = KeyReader::new();
    let _ = final_reader.get_key();
}

