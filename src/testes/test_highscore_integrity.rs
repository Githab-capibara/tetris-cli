//! Тесты проверки целостности при загрузке (highscore.rs).
//!
//! Этот модуль содержит 3 теста для проверки исправления:
//! - Проверка assert_hs() с валидными данными
//! - Проверка обнаружения подделки рекорда
//! - Проверка целостности LeaderboardEntry
//!
//! Проверка целостности защищает от модификации файлов сохранений.

use crate::highscore::{LeaderboardEntry, SaveData};

// ============================================================================
// ГРУППА ТЕСТОВ: Проверка целостности при загрузке
// ============================================================================

/// Тест 1: Проверка assert_hs() с валидными данными
///
/// Проверяет, что валидный рекорд проходит проверку целостности.
#[test]
fn test_целостность_с_валидными_данными() {
    // Создаём валидный рекорд
    let save = SaveData::from_value(5000);

    // Проверяем что хеш валиден
    let result = save.assert_hs();
    assert_eq!(
        result, 5000,
        "Валидный рекорд должен вернуть правильное значение"
    );

    // Проверяем что значение корректно
    assert!(
        result > 0,
        "Результат должен быть положительным для валидного рекорда"
    );
}

/// Тест 2: Проверка обнаружения подделки рекорда
///
/// Проверяет, что модификация рекорда обнаруживается через assert_hs().
#[test]
fn test_обнаружение_подделки_рекорда() {
    // Создаём валидный рекорд
    let save = SaveData::from_value(1000);

    // Проверяем что изначально он валиден
    assert_eq!(
        save.assert_hs(),
        1000,
        "Оригинальный рекорд должен быть валидным"
    );

    // Создаём рекорд с другим значением для сравнения
    let save_different = SaveData::from_value(99999);

    // Проверяем что разные значения дают разные результаты
    assert_ne!(
        save.assert_hs(),
        save_different.assert_hs(),
        "Разные рекорды должны иметь разные значения"
    );

    // Тест 2: Проверяем что SaveData::default() возвращает 0
    let save_default = SaveData::default();
    assert_eq!(
        save_default.assert_hs(),
        0,
        "Рекорд по умолчанию должен вернуть 0"
    );
}

/// Тест 3: Проверка целостности LeaderboardEntry
///
/// Проверяет, что is_valid() корректно работает для записей.
#[test]
fn test_целостность_записи_таблицы_лидеров() {
    // Тест 1: Валидная запись
    let entry = LeaderboardEntry::new("Player".to_string(), 5000);
    assert!(entry.is_valid(), "Новая запись должна быть валидной");

    // Тест 2: Проверяем что запись содержит правильные данные
    assert_eq!(entry.name, "Player", "Имя игрока должно совпадать");
    assert_eq!(entry.score, 5000, "Очки должны совпадать");

    // Тест 3: Проверяем что хеш не пустой
    assert!(!entry.hash.is_empty(), "Хеш не должен быть пустым");

    // Тест 4: Создаём запись с подделанными данными (через new с другими данными)
    let entry2 = LeaderboardEntry::new("Player".to_string(), 1000);
    assert!(entry2.is_valid(), "Вторая запись тоже должна быть валидной");

    // Но хеши должны быть разными из-за разной соли
    assert_ne!(
        entry.hash, entry2.hash,
        "Хеши должны быть разными из-за уникальной соли"
    );
}
