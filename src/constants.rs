//! Централизованный модуль констант игры Tetris CLI.
//!
//! Этот модуль содержит ВСЕ константы проекта для устранения дублирования.
//! Все остальные модули должны импортировать константы отсюда.
//!
//! # Организация
//! Константы сгруппированы по категориям:
//!
//! ### Базовые константы
//! - `BORDER_COLOR` - цвет границ
//! - `FPS` - количество кадров в секунду
//! - `FRAME_DELAY_MS` - задержка между кадрами
//!
//! ### Физика игры
//! - `INITIAL_FALL_SPD` - начальная скорость падения
//! - `MAX_FALL_SPEED` - максимальная скорость
//! - `SPD_INC` - прирост скорости
//! - `LAND_TIME_DELAY_S` - задержка приземления
//!
//! ### Система очков
//! - `LINE_SCORES` - очки за линии
//! - `COMBO_BONUS` - бонус за комбо
//! - `LEVEL_BONUS_MULT` - бонус за уровень
//!
//! ### Размеры игрового поля
//! - `GRID_WIDTH` - ширина поля в блоках
//! - `GRID_HEIGHT` - высота поля в блоках
//! - `SHAPE_WIDTH` - ширина блока в символах
//! - `SHAPE_STR` - строковое представление блока
//!
//! ### Позиции отрисовки UI
//! - `SCORE_X`, `SCORE_Y` - позиция счёта
//! - `HIGH_SCORE_X`, `HIGH_SCORE_Y` - позиция рекорда
//! - `LEVEL_X`, `LEVEL_Y` - позиция уровня
//! - `LINES_X`, `LINES_Y` - позиция линий
//! - `PREVIEW_X`, `PREVIEW_Y` - позиция следующей фигуры
//! - `HOLD_PREVIEW_X`, `HOLD_PREVIEW_Y` - позиция удержанной фигуры
//!
//! ### Константы игры
//! - `MAX_LINES_PER_CLEAR` - максимум линий за ход
//! - `SPRINT_LINES` - линии для спринта
//! - `MARATHON_LINES` - линии для марафона
//! - `MIN_Y` - минимальная координата Y
//!
//! ### Константы клавиш
//! - `KEY_BACKSPACE` - код Backspace
//! - `KEY_ENTER_LF`, `KEY_ENTER_CR` - коды Enter
//! - `KEY_ESCAPE` - код Escape
//! - `KEY_SPACE` - код пробела
//!
//! ## Использование
//! ```
//! use tetris_cli::constants::GRID_WIDTH;
//! use tetris_cli::constants::FRAME_DELAY_MS;
//! ```

