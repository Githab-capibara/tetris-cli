/// Получить линию границы по индексу.
///
/// # Аргументы
/// * `line_index` - индекс линии (0..DISP_HEIGHT)
///
/// # Возвращает
/// Строку границы или пустую строку если индекс вне диапазона
///
/// # Пример
/// ```ignore
/// use crate::constants::get_border_line;
/// let top_border = get_border_line(5);
/// ```
///
/// # Panics
/// Никогда не паникует. Возвращает пустую строку при невалидном индексе.
#[must_use]
#[inline]
pub const fn get_border_line(line_index: usize) -> &'static str {
    if line_index < BORDER.len() {
        BORDER[line_index]
    } else {
        ""
    }
}

