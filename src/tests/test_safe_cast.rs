/// Тест 13: Интеграционный тест `safe_f32_to_u32()` в контексте Hard Drop.
///
/// Проверяет что `safe_f32_to_u32()` корректно работает в контексте
/// расчёта расстояния Hard Drop.
#[test]
fn test_safe_f32_to_u32_hard_drop_context() {
    // Симуляция расчёта расстояния Hard Drop
    let start_y = 0.0;
    let end_y_values: [f32; 5] = [5.0, 10.0, 15.0, 20.0, 25.0];

    for &end_y in end_y_values.iter() {
        let drop_distance_f32 = (end_y - start_y).abs();
        let drop_distance = safe_f32_to_u32(drop_distance_f32);

        assert_eq!(
            drop_distance, end_y as u32,
            "Расстояние Hard Drop должно конвертироваться корректно"
        );
    }

    // Тест с отрицательным расстоянием (должно использовать abs)
    let start_y_neg: f32 = 10.0;
    let end_y_neg: f32 = 5.0;
    let drop_distance_f32 = (end_y_neg - start_y_neg).abs();
    let drop_distance = safe_f32_to_u32(drop_distance_f32);
    assert_eq!(
        drop_distance, 5,
        "Отрицательное расстояние должно конвертироваться через abs"
    );

    println!("✓ Интеграционный тест Hard Drop пройден успешно");
}

/// Тест 14: Проверка точности границы `4_294_967_295.0`.
///
/// Проверяет что используется точная граница вместо `u32::MAX` as f32.
///
/// Примечание: f32 не может точно представить `4_294_967_294.0`,
/// поэтому тесты с точными целыми числами ~2^32 ограничены.
#[test]
fn test_safe_f32_to_u32_exact_boundary() {
    // Точная граница
    const EXACT_MAX: f32 = 4_294_967_295.0;

    // Проверка что точная граница конвертируется в u32::MAX
    assert_eq!(
        safe_f32_to_u32(EXACT_MAX),
        u32::MAX,
        "Точная граница 4_294_967_295.0 должна конвертироваться в u32::MAX"
    );

    // Примечание: EXACT_MAX - 1.0 = 4294967294.0 округляется до 4294967296.0 в f32,
    // что >= MAX_SAFE_F32_AS_U32, поэтому saturates в u32::MAX.
    // Это ожидаемое поведение из-за ограничения точности f32.
    let just_below = EXACT_MAX - 1.0;
    assert_eq!(
        safe_f32_to_u32(just_below),
        u32::MAX,
        "f32 округляет 4_294_967_294.0 → saturates в u32::MAX"
    );

    // Значение на 1 больше границы saturating cast в u32::MAX
    assert_eq!(
        safe_f32_to_u32(EXACT_MAX + 1.0),
        u32::MAX,
        "4_294_967_296.0 должен saturating cast в u32::MAX"
    );

    // Значение значительно меньше границы — должно работать точно
    let smaller_val: f32 = 1_000_000_000.0; // 1e9, точно представимо в f32
    assert_eq!(
        safe_f32_to_u32(smaller_val),
        1_000_000_000,
        "1e9 должна конвертироваться точно"
    );

    println!("✓ Точная граница 4_294_967_295.0 работает корректно");
}
