/// Тест 8: Проверка конвертации значения `u32::MAX` + 1.
///
/// Проверяет что значения больше `u32::MAX` saturating cast в `u32::MAX`.
#[test]
fn test_safe_f32_to_u32_u32_max_plus_one() {
    // Тест значения u32::MAX + 1
    // Из-за потери точности f32, u32::MAX as f32 + 1.0 может быть равно u32::MAX as f32
    // Поэтому проверяем что результат saturating cast в u32::MAX
    let overflow_value = (u32::MAX as f32) + 1.0;
    assert_eq!(
        safe_f32_to_u32(overflow_value),
        u32::MAX,
        "u32::MAX + 1 должен saturating cast в u32::MAX"
    );

    println!("✓ u32::MAX + 1 конвертируется в u32::MAX (saturating cast)");
}

/// Тест 9: Проверка конвертации значений вокруг границы `u32::MAX`.
///
/// Проверяет что значения вокруг границы обрабатываются корректно.
///
/// Примечание: f32 имеет только 24 бита мантиссы, поэтому числа
/// вблизи `u32::MAX` (32 бита) не могут быть точно представлены.
/// `4_294_967_294.0` округляется до `4_294_967_296.0` в f32.
#[test]
fn test_safe_f32_to_u32_around_u32_max() {
    // Тест значения чуть меньше u32::MAX
    let just_below_max = (u32::MAX as f32) - 1.0;
    let result = safe_f32_to_u32(just_below_max);
    assert!(
        result >= u32::MAX - 256,
        "Значение чуть меньше u32::MAX должно конвертироваться в значение >= u32::MAX - 256"
    );

    // Примечание: 4_294_967_294.0 НЕ может быть точно представлено в f32.
    // При cast в f32 оно округляется до 4294967296.0, что >= MAX_SAFE_F32_AS_U32.
    // Поэтому результат — u32::MAX.
    let val: f32 = 4_294_967_294.0;
    // Проверяем что f32 округлил значение
    assert!(
        val >= 4_294_967_295.0f32,
        "4_294_967_294.0 округляется в f32 до >= 4_294_967_295.0"
    );
    assert_eq!(
        safe_f32_to_u32(val),
        u32::MAX,
        "Округлённое значение saturates в u32::MAX"
    );

    println!("✓ Значения вокруг u32::MAX конвертируются корректно");
}

