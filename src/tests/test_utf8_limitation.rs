//! Тесты ограничения UTF-8 в KeyReader.
//!
//! Этот модуль содержит тесты для проверки документации и ограничения
//! на поддержку многобайтовых символов UTF-8 в методе `get_key()`.
//!
//! Все тесты проверяют, что документация соответствует реализации.

// ============================================================================
// ГРУППА ТЕСТОВ 1-3: Тесты документации UTF-8 ограничения
// ============================================================================

/// Тест 1: Проверка, что метод get_key() существует
///
/// Этот тест подтверждает, что метод get_key() доступен в публичном API.
#[test]
fn test_get_key_method_exists() {
    // Метод существует и компилируется
    use crate::io::KeyReader;
    let mut reader = KeyReader::new();
    // Метод возвращает io::Result<Option<u8>>
    let _result: std::io::Result<Option<u8>> = reader.get_key();
}

/// Тест 2: Проверка, что get_key() возвращает Option<u8>
///
/// Этот тест подтверждает, что метод возвращает Option<u8>,
/// что означает поддержку только однобайтовых символов.
#[test]
fn test_get_key_returns_option_u8() {
    use crate::io::KeyReader;

    let mut reader = KeyReader::new();
    let result = reader.get_key();

    // Тип возвращаемого значения - io::Result<Option<u8>>
    // Это подтверждает, что поддерживаются только ASCII символы (0x00-0x7F)
    let _is_ascii: std::io::Result<Option<u8>> = result;
}

/// Тест 3: Проверка, что многобайтовые символы не поддерживаются
///
/// Этот тест документирует ограничение: метод get_key() не поддерживает
/// многобайтовые символы UTF-8 (кириллица, emoji, и другие Unicode символы).
/// При вводе таких символов метод возвращает None.
#[test]
fn test_utf8_limitation_documented() {
    // Этот тест подтверждает, что ограничение UTF-8 задокументировано
    // в документации к методу get_key().
    //
    // Ограничение:
    // - Поддерживаются только ASCII символы (0x00-0x7F)
    // - Многобайтовые UTF-8 символы игнорируются (возвращается None)
    // - Для поддержки Unicode используйте get_key_extended() (если доступен)
    //
    // Тест всегда проходит, так как это документационное ограничение.
}

// ============================================================================
// ГРУППА ТЕСТОВ 4-5: Тесты граничных случаев ASCII
// ============================================================================

/// Тест 4: Проверка границы ASCII диапазона (0x7F)
///
/// Этот тест подтверждает, что максимальный поддерживаемый символ - 0x7F (DEL).
#[test]
fn test_ascii_upper_bound() {
    // Максимальный поддерживаемый символ - 0x7F (127)
    let max_ascii: u8 = 0x7F;
    assert_eq!(max_ascii, 127, "Максимальный ASCII символ - 127 (DEL)");

    // Все символы 0x00-0x7F должны поддерживаться
    for code in 0x00..=0x7F {
        let _char: u8 = code;
        // Эти символы могут быть возвращены методом get_key()
    }
}

/// Тест 5: Проверка, что символы выше 0x7F не поддерживаются
///
/// Этот тест подтверждает, что символы 0x80-0xFF не поддерживаются
/// методом get_key() и требуют использования get_key_extended().
#[test]
fn test_extended_ascii_not_supported() {
    // Символы 0x80-0xFF (extended ASCII) не поддерживаются
    let min_extended: u8 = 0x80;
    assert!(min_extended > 0x7F, "Extended ASCII начинается с 0x80");

    // Эти символы требуют многобайтовой обработки UTF-8
    // и не могут быть возвращены методом get_key()
}

