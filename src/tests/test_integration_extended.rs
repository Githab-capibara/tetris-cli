/// Тест: Marathon режим поддерживает сохранение рекорда.
#[test]
fn test_marathon_mode_saves_score() {
    let state = GameState::new_marathon();

    assert_eq!(
        state.get_mode_trait().name(),
        "Марафон",
        "Режим должен быть Marathon"
    );
}

