/// Тест 14: Проверка что макросы не паникуют
///
/// Проверяет, что макросы безопасны и не вызывают панику.
#[test]
fn test_log_macros_no_panic() {
    let result = std::panic::catch_unwind(|| {
        log_error!("Тест");
        log_warn!("Тест");
        log_info!("Тест");
    });

    assert!(result.is_ok(), "Макросы не должны вызывать панику");
}
