/// Тест 7: Проверка работы макросов с разными типами данных
///
/// Проверяет, что макросы поддерживают разные типы параметров.
#[test]
fn test_log_macros_with_different_types() {
    // Строки
    log_error!("Строка: {}", "текст");
    log_warn!("Строка: {}", "текст");
    log_info!("Строка: {}", "текст");

    // Числа
    log_error!(
        "i32: {}, u64: {}, f64: {}",
        42i32,
        100u64,
        std::f64::consts::PI
    );
    log_warn!(
        "i32: {}, u64: {}, f64: {}",
        42i32,
        100u64,
        std::f64::consts::PI
    );
    log_info!(
        "i32: {}, u64: {}, f64: {}",
        42i32,
        100u64,
        std::f64::consts::PI
    );

    // Булевы значения
    log_error!("Булево: {}", true);
    log_warn!("Булево: {}", false);
    log_info!("Булево: {}", true);
}

