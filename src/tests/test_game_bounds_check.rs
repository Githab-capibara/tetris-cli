/// Тест 1: Проверка безопасной конвертации расстояния падения
///
/// Проверяет, что конвертация f32 в u64 использует max(0.0)
/// для предотвращения некорректной конвертации отрицательных чисел.
#[test]
fn test_safe_fall_distance_conversion() {
    // Тестируем логику конвертации напрямую
    let start_y: f32 = 10.0;
    let curr_y: f32 = 10.0;

    // Проверяем что конвертация работает корректно
    // drop_distance = (curr_y - start_y).max(0.0) as u64
    let drop_distance = (curr_y - start_y).max(0.0) as u64;
    assert_eq!(
        drop_distance, 0,
        "Расстояние падения без движения должно быть 0"
    );

    // Симулируем падение на 5 ячеек вниз
    let curr_y_after = start_y + 5.0;
    let drop_distance = (curr_y_after - start_y).max(0.0) as u64;
    assert_eq!(
        drop_distance, 5,
        "Расстояние падения должно быть 5 после движения вниз"
    );
}

/// Тест 2: Проверка обработки отрицательных значений при конвертации
///
/// Проверяет, что max(0.0) корректно обрабатывает случаи,
/// когда разница координат отрицательна.
#[test]
fn test_negative_values_conversion_handling() {
    let start_y: f32 = 10.0;

    // Симулируем ситуацию где фигура "выше" начальной позиции
    let curr_y = start_y - 3.0;

    // Конвертация должна вернуть 0 благодаря max(0.0)
    let drop_distance = (curr_y - start_y).max(0.0) as u64;
    assert_eq!(
        drop_distance, 0,
        "Отрицательное расстояние должно быть обрезано до 0"
    );

    // Проверяем что конвертация не вызывает панику
    // даже с очень большими отрицательными значениями
    let curr_y_large = start_y - 1000.0;
    let drop_distance = (curr_y_large - start_y).max(0.0) as u64;
    assert_eq!(
        drop_distance, 0,
        "Большое отрицательное расстояние должно быть обрезано до 0"
    );
}

/// Тест 3: Проверка граничных значений конвертации
///
/// Проверяет конвертацию различных граничных значений:
/// - Ноль
/// - Максимально возможное расстояние
/// - Дробные значения
#[test]
fn test_conversion_boundary_values() {
    // Тест 1: Нулевое значение
    let zero: f32 = 0.0;
    let zero_converted = zero.max(0.0) as u64;
    assert_eq!(zero_converted, 0, "0.0 должно конвертироваться в 0");

    // Тест 2: Очень малое положительное значение
    let small: f32 = 0.1;
    let small_converted = small.max(0.0) as u64;
    assert_eq!(
        small_converted, 0,
        "0.1 должно конвертироваться в 0 (округление вниз)"
    );

    // Тест 3: Значение ровно 1.0
    let one: f32 = 1.0;
    let one_converted = one.max(0.0) as u64;
    assert_eq!(one_converted, 1, "1.0 должно конвертироваться в 1");

    // Тест 4: Большое значение (симуляция большого падения)
    let large: f32 = 20.0; // Максимальная высота поля ~20
    let large_converted = large.max(0.0) as u64;
    assert_eq!(large_converted, 20, "20.0 должно конвертироваться в 20");

    // Тест 5: Дробное значение (проверка округления)
    let fractional: f32 = 5.9;
    let fractional_converted = fractional.max(0.0) as u64;
    assert_eq!(
        fractional_converted, 5,
        "5.9 должно конвертироваться в 5 (отбрасывание дроби)"
    );
}
