/// Проверка атомарности get_valid_score() для LeaderboardEntry.
///
/// ## Предотвращаемая проблема: CRITICAL
/// TOCTOU (Time-Of-Check-Time-Of-Use) уязвимость может позволить
/// злоумышленнику модифицировать данные между проверкой и использованием.
///
/// ## Что проверяет:
/// - `get_valid_score()` выполняет атомарную проверку и возврат
/// - Валидная запись возвращает Some(score)
/// - Метод защищает от гонок данных в многопоточной среде
#[test]
fn test_leaderboard_entry_atomic_validation() {
    use crate::highscore::leaderboard::LeaderboardEntry;

    let entry = LeaderboardEntry::new("Test", 1000);

    // Атомарная проверка и получение
    let valid_score = entry.get_valid_score();
    assert_eq!(
        valid_score,
        Some(1000),
        "Валидная запись должна вернуть score"
    );

    // Проверка что score() тоже возвращает корректное значение
    let score = entry.score();
    assert_eq!(score, 1000, "score() должен вернуть 1000");

    // Проверка что is_valid() возвращает true
    assert!(entry.is_valid(), "Запись должна быть валидной");
}

