/// Проверка что saturating операции работают корректно при больших значениях.
///
/// ## Предотвращаемая проблема: CRITICAL
/// Переполнение u128 при начислении очков может привести к некорректному
/// подсчёту рекордов и потенциальной уязвимости.
///
/// ## Что проверяет:
/// - `saturating_mul` предотвращает переполнение при умножении
/// - При достижении u128::MAX происходит насыщение вместо паники
/// - Операции с большими числами работают корректно
#[test]
fn test_score_overflow_protection() {
    // Проверка saturating_mul при больших значениях
    let base = u128::MAX / 2;
    let multiplier = 3u128;
    let result = base.saturating_mul(multiplier);
    assert_eq!(result, u128::MAX, "Должно быть насыщение до MAX");
    
    // Дополнительная проверка: умножение на 0
    let zero_result = u128::MAX.saturating_mul(0);
    assert_eq!(zero_result, 0, "Умножение на 0 должно дать 0");
    
    // Проверка saturating_add
    let add_result = u128::MAX.saturating_add(1);
    assert_eq!(add_result, u128::MAX, "Сложение должно насыщаться до MAX");
}

