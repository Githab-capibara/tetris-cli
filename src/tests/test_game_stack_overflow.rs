//! Тесты проверки переполнения стека (game.rs).
//!
//! Этот модуль содержит 3 теста для проверки исправления:
//! - blocks теперь размещается в куче через Box вместо стека
//! - Предотвращение переполнения стека при инициализации
//! - Проверка работы с большим игровым полем
//!
//! Исправление: использование Box<[[i8; `GRID_WIDTH`]; `GRID_HEIGHT`]> вместо [[i8; `GRID_WIDTH`]; `GRID_HEIGHT`]

use crate::game::GameState;
use crate::io::{GRID_HEIGHT, GRID_WIDTH};

// ============================================================================
// ГРУППА ТЕСТОВ: Переполнение стека
// ============================================================================

/// Тест 1: Проверка инициализации поля в куче
///
/// Проверяет, что `GameState::new()` успешно создаёт игровое состояние
/// с полем blocks, размещённым в куче через Box.
#[test]
fn test_инициализация_поля_в_куче() {
    // Создаём новое состояние игры
    // Это должно работать без переполнения стека благодаря Box
    let game_state = GameState::new();

    // Проверяем что состояние создано корректно
    // (тест компилируется только если blocks использует Box)
    let _ = &game_state;

    // Косвенная проверка: создаём несколько состояний подряд
    // Если бы blocks был на стеке, это могло бы вызвать переполнение
    let state1 = GameState::new();
    let state2 = GameState::new();
    let state3 = GameState::new();

    // Все три состояния должны быть созданы успешно
    let _ = &state1;
    let _ = &state2;
    let _ = &state3;

    // Проверяем что поля инициализированы правильными размерами
    // Используем константы для проверки
    const _: () = assert!(GRID_WIDTH > 0, "GRID_WIDTH должен быть положительным");
    const _: () = assert!(GRID_HEIGHT > 0, "GRID_HEIGHT должен быть положительным");

    // Проверяем что get_blocks() возвращает ссылку на Box
    let blocks = game_state.get_blocks();
    assert!(
        blocks.len() == GRID_HEIGHT,
        "Количество строк должно быть {GRID_HEIGHT}"
    );
    assert!(
        blocks[0].len() == GRID_WIDTH,
        "Количество столбцов должно быть {GRID_WIDTH}"
    );
}

/// Тест 2: Проверка работы с большим полем
///
/// Проверяет, что можно создавать множество состояний игры
/// без исчерпания стека благодаря размещению в куче.
#[test]
fn test_работа_с_большим_полем() {
    // Создаём массив из 100 состояний игры
    // Это было бы невозможно при размещении на стеке
    let mut states: Vec<GameState> = Vec::with_capacity(100);

    for i in 0..100 {
        let state = GameState::new();
        // Проверяем что состояние создано корректно
        assert!(
            state.get_level() >= 1,
            "Состояние {i} должно иметь уровень >= 1"
        );
        states.push(state);
    }

    // Проверяем что все 100 состояний созданы успешно
    assert_eq!(states.len(), 100, "Должно быть создано 100 состояний");

    // Проверяем что каждое состояние имеет корректные размеры поля
    for (i, state) in states.iter().enumerate() {
        let blocks = state.get_blocks();
        assert!(
            blocks.len() == GRID_HEIGHT,
            "Состояние {i} должно иметь {GRID_HEIGHT} строк"
        );
        assert!(
            blocks[0].len() == GRID_WIDTH,
            "Состояние {i} должно иметь {GRID_WIDTH} столбцов"
        );
    }
}

/// Тест 3: Проверка отсутствия переполнения стека
///
/// Стресс-тест: создание множества состояний игры в цикле
/// для проверки отсутствия переполнения стека.
#[test]
fn test_отсутствие_переполнения_стека() {
    use std::time::Instant;

    let start = Instant::now();

    // Создаём 1000 состояний игры в цикле
    // При размещении в куче это должно работать без проблем
    for _ in 0..1000 {
        let state = GameState::new();
        // Немного используем состояние чтобы избежать оптимизации
        let _level = state.get_level();
    }

    let elapsed = start.elapsed();

    // Проверяем что выполнение заняло разумное время (< 5 секунд)
    assert!(
        elapsed.as_secs() < 5,
        "Создание 1000 состояний должно занять меньше 5 секунд"
    );

    // Дополнительная проверка: создаём вложенные состояния
    // Это было бы проблематично при стековом размещении
    fn create_nested_states(depth: usize) -> GameState {
        if depth == 0 {
            GameState::new()
        } else {
            let _inner = create_nested_states(depth - 1);
            GameState::new()
        }
    }

    // Создаём 10 уровней вложенности
    let _nested_state = create_nested_states(10);

    // Если тест дошёл до этой точки, переполнения стека не произошло
}
