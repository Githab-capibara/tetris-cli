// Тип usize используется для совместимости с массивами.

/// Полная ширина дисплея (поле + границы + UI).
/// Формула: (`SHAPE_WIDTH` * `GRID_WIDTH`) + 2 (границы) = (2 * 10) + 2 = 22
pub const DISP_WIDTH: usize = (SHAPE_WIDTH * GRID_WIDTH) + 2;

/// Полная высота дисплея.
/// Формула: `GRID_HEIGHT` + 5 (заголовки и границы) = 20 + 5 = 25
pub const DISP_HEIGHT: usize = GRID_HEIGHT + 5;

