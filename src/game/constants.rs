/// Позиция X для отрисовки счёта (строка 2).
///
/// # Обоснование значения
/// Значение 7 выбрано как отступ от левого края поля:
/// - 1 (левая граница поля) + 6 (длина строки "Счёт: ") = 7
///
/// # Расположение
/// Размещается во второй строке массива `BORDER` сразу после заголовка.
pub const SCORE_X: u16 = 7;

/// Позиция Y для отрисовки счёта (строка 2).
///
/// # Обоснование значения
/// Значение 2 выбрано как отступ от верха:
/// - Строка 2 в массиве `BORDER` (индексация с 1)
/// - Расположение сразу после верхней границы игрового поля
///
/// # Расположение
/// Размещается во второй строке дисплея для видимости счёта игроком.
pub const SCORE_Y: u16 = 2;

/// Позиция X для отрисовки рекорда (строка 3).
///
/// # Обоснование значения
/// Значение 7 выбрано как отступ от левого края поля:
/// - 1 (левая граница) + 6 (длина "Рекорд: ") = 7
///
/// # Расположение
/// Размещается под счётом для удобства сравнения текущего счёта с рекордом.
pub const HIGH_SCORE_X: u16 = 7;

/// Позиция Y для отрисовки рекорда (строка 3).
///
/// # Обоснование значения
/// Значение 3 выбрано как строка 3 в массиве `BORDER`:
/// - Расположение сразу под счётом ([`SCORE_Y`] + 1)
///
/// # Расположение
/// Третья строка дисплея для логической группировки счёта и рекорда.
pub const HIGH_SCORE_Y: u16 = 3;

/// Позиция X для отрисовки уровня (строка 4).
///
/// # Обоснование значения
/// Значение 10 выбрано как отступ от левого края поля:
/// - 1 (левая граница) + 9 (длина "Уровень: ") = 10
///
/// # Расположение
/// Размещается под рекордом для логической группировки статистики.
pub const LEVEL_X: u16 = 10;

/// Позиция Y для отрисовки уровня (строка 4).
///
/// # Обоснование значения
/// Значение 4 выбрано как строка 4 в массиве `BORDER`:
/// - Расположение сразу под рекордом ([`HIGH_SCORE_Y`] + 1)
///
/// # Расположение
/// Четвёртая строка дисплея для последовательного отображения статистики.
pub const LEVEL_Y: u16 = 4;

/// Позиция X для отрисовки линий (строка 5).
///
/// # Обоснование значения
/// Значение 10 выбрано как отступ от левого края поля:
/// - 1 (левая граница) + 9 (длина "Линии: ") = 10
///
/// # Расположение
/// Размещается под уровнем для логической группировки статистики.
pub const LINES_X: u16 = 10;

/// Позиция Y для отрисовки линий (строка 5).
///
/// # Обоснование значения
/// Значение 5 выбрано как строка 5 в массиве `BORDER`:
/// - Расположение сразу под уровнем ([`LEVEL_Y`] + 1)
///
/// # Расположение
/// Пятая строка дисплея для завершения блока статистики.
pub const LINES_Y: u16 = 5;

/// Позиция предпросмотра следующей фигуры по X (справа от поля).
///
/// # Обоснование значения
/// Значение 24 выбрано как правый край от поля:
/// - [`GRID_WIDTH`] (10) + 2 (границы поля) + 5 (отступ) + 7 (доп. отступ) = 24
/// - Альтернативно: [`DISP_WIDTH`] (22) + 2 (отступ от правой границы) = 24
///
/// # Расположение
/// Справа от игрового поля для удобства просмотра игроком.
pub const PREVIEW_X: u16 = 24;

/// Позиция предпросмотра следующей фигуры по Y.
///
/// # Обоснование значения
/// Значение 8 выбрано как строка 8 для размещения:
/// - Под блоком основной статистики ([`LINES_Y`] + 3 = 8)
/// - Достаточно места для отрисовки фигуры 4x4
///
/// # Расположение
/// В правой панели UI, под основной статистикой.
pub const PREVIEW_Y: u16 = 8;

/// Позиция предпросмотра удержанной фигуры по X (слева от поля).
///
/// # Обоснование значения
/// Значение 2 выбрано как левый край от поля:
/// - 1 (левая граница) + 1 (отступ) = 2
///
/// # Расположение
/// Слева от игрового поля для симметрии с [`PREVIEW_X`].
pub const HOLD_PREVIEW_X: u16 = 2;

/// Позиция предпросмотра удержанной фигуры по Y.
///
/// # Обоснование значения
/// Значение 8 выбрано для симметрии с [`PREVIEW_Y`]:
/// - Одинаковая высота с следующей фигурой для баланса UI
///
/// # Расположение
/// В левой панели UI, на той же высоте что и следующая фигура.
pub const HOLD_PREVIEW_Y: u16 = 8;

/// Позиция X для отрисовки комбо.
///
/// # Обоснование значения
/// Значение 24 выбрано аналогично [`PREVIEW_X`]:
/// - [`DISP_WIDTH`] (22) + 2 (отступ) = 24
/// - Размещение в правой панели UI
///
/// # Расположение
/// В правой панели UI, под предпросмотром следующей фигуры.
pub const COMBO_X: u16 = 24;

/// Позиция Y для отрисовки комбо.
///
/// # Обоснование значения
/// Значение 6 выбрано как строка 6 для размещения:
/// - Под блоком основной статистики ([`LINES_Y`] + 1 = 6)
/// - Над предпросмотром следующей фигуры ([`PREVIEW_Y`] - 2 = 6)
///
/// # Расположение
/// В правой панели UI, между статистикой и предпросмотром.
pub const COMBO_Y: u16 = 6;

/// Позиция Y для отрисовки таймера (режим спринт).
///
/// # Обоснование значения
/// Значение 20 выбрано как строка 20 в правой панели UI:
/// - Размещение в нижней части правой панели
/// - Достаточно места для отображения времени
///
/// # Расположение
/// В правой панели UI, используется только в режиме спринт.
pub const TIMER_Y: u16 = 20;

/// Позиция Y для отрисовки прогресса (режим спринт).
///
/// # Обоснование значения
/// Значение 21 выбрано как строка 21, сразу под таймером:
/// - [`TIMER_Y`] + 1 = 21
/// - Логическая группировка таймера и прогресса
///
/// # Расположение
/// В правой панели UI, под таймером для режима спринт.
pub const PROGRESS_Y: u16 = 21;

