//! Модуль констант игры.
//!
//! Централизованное хранилище всех констант для устранения дублирования.

use termion::color::White;

// ============================================================================
// БАЗОВЫЕ КОНСТАНТЫ
// ============================================================================

/// Цвет границ.
pub const BORDER_COLOR: &dyn termion::color::Color = &White;

/// Количество кадров в секунду.
///
/// Обеспечивает плавную анимацию игры.
/// Стандартное значение для большинства игр - 60 FPS.
pub const FPS: u64 = 60;

/// Задержка между кадрами в миллисекундах.
/// Вычисляется как 1000 / FPS ≈ 16.67 мс.
/// TODO: использовать в будущей рефакторизации FPS контроллера
#[allow(dead_code)]
pub const FRAME_DELAY_MS: u64 = 1000 / FPS;

/// Миллисекунд в секунде.
pub const MILLIS_PER_SECOND: f32 = 1000.0;

// ============================================================================
// ФИЗИКА ИГРЫ
// ============================================================================

/// Начальная скорость падения фигур (блоков в секунду).
pub const INITIAL_FALL_SPD: f32 = 0.9;

/// Максимальная скорость падения фигур.
///
/// Ограничивает максимальную скорость фигуры для предотвращения переполнения
/// при расчёте очков за падение.
pub const MAX_FALL_SPEED: f32 = 1000.0;

/// Прирост скорости падения за каждую удалённую линию.
pub const SPD_INC: f32 = 0.05;

/// Задержка приземления фигуры в секундах.
///
/// Даёт игроку время на перемещение фигуры после касания.
pub const LAND_TIME_DELAY_S: f64 = 0.1;

// ============================================================================
// СИСТЕМА ОЧКОВ
// ============================================================================

/// Очки за удаление линий [1, 2, 3, 4 линии].
/// Формула: 100 × 2^(n-1) для n < 4, 1800 для n = 4 (Tetris)
pub const LINE_SCORES: [u128; 4] = [
    100,  // 1 линия: 100 × 2^0 = 100
    200,  // 2 линии: 100 × 2^1 = 200
    400,  // 3 линии: 100 × 2^2 = 400
    1800, // 4 линии (Tetris): 100 × 2^3 + 1000 = 1800
];

/// Бонус за комбо (за каждую последовательную линию).
pub const COMBO_BONUS: u128 = 50;

/// Бонус за уровень (умножается на текущий уровень).
pub const LEVEL_BONUS_MULT: u128 = 500;

