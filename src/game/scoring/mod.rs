//! Модуль системы очков и уровней.
//!
//! # Ответственность
//! - Начисление очков за фигуры и линии
//! - Повышение уровня
//! - Управление комбо
//! - Поиск и удаление заполненных линий
//!
//! # Подмодули
//! - [`lines`] - поиск и удаление заполненных линий
//! - [`points`] - начисление очков и обновление уровня
//! - [`combo`] - комбо-система и бонусы (базовая функция)
//!
//! # Зависимости
//! - [`state.rs`](super::state): константы очков, `GameState`
//! - [`tetromino.rs`](crate::tetromino): `Tetromino`
//!
//! ## Архитектурные заметки
//! ## Исправление #23
//! Функции комбо-системы были сокращены до базовой `calculate_combo_bonus()`.
//! Остальные функции удалены как неиспользуемые (YAGNI).
//!
//! ## Таблица очков
//! | Действие | Формула | Пример |
//! |----------|---------|--------|
//! | 1 линия | 100 × 2^0 | 100 |
//! | 2 линии | 100 × 2^1 | 200 |
//! | 3 линии | 100 × 2^2 | 400 |
//! | 4 линии (Tetris) | 100 × 2^3 + 1000 | 1800 |
//! | Soft Drop | 1 × ячейки | 1 за ячейку |
//! | Hard Drop | 2 × ячейки | 2 за ячейку |
//! | Комбо | 50 × (комбо - 1) | 50 за 2-е комбо |
//! | Повышение уровня | 500 × (уровень - 1) | 500 за уровень 2 |
//!
//! ## Архитектурные заметки
//! ## Разделение ответственности (Problem 2.3, 2.9)
//! Этот модуль отвечает ТОЛЬКО за начисление очков и управление линиями.
//!
//! ## Исправление #6 (HIGH): Trait ScoringState
//! Добавлен trait `ScoringState` для инкапсуляции изменений состояния.
//! Это предотвращает прямой доступ к полям GameState и улучшает инкапсуляцию.
//!
//! ## ISP-1: Разделение на узкие трейты
//! ScoringState разделён на узкие трейты для соблюдения Interface Segregation Principle:
//! - [`ScoreAccess`] - доступ к очкам (get_score, add_score)
//! - [`LevelAccess`] - доступ к уровням (get_level, set_level)
//! - [`LinesAccess`] - доступ к линиям (get_lines_cleared, add_lines)
//! - [`ComboAccess`] - доступ к комбо (get_combo, reset_combo)
//!
//! TODO (#архитектура): Избегать прямого изменения полей GameState.
//! В настоящее время используется прямой доступ к полям для производительности.
//! Рассмотреть возможность использования методов GameState для модификации.
//!
//! ## Доступ к полям GameState
//! Модуль scoring.rs имеет доступ к `pub(crate)` полям GameState,
//! так как находится в том же модуле game. Это допустимо для внутренней логики,
//! но для нового кода рекомендуется использовать геттеры/сеттеры.
//!
//! ## Архитектурное улучшение 2026-04-01 (DRY2)
//! ScoreAccess и другие трейты импортируются из `super::access` для устранения дублирования.
//! Этот модуль делает re-export для обратной совместимости.

