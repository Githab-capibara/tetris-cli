//! # Tetris CLI — Классическая игра Тетрис для терминала
//!
//! ## 📖 Описание проекта
//!
//! Tetris CLI — это классическая игра Тетрис, реализованная на Rust для терминала.
//! Поддерживает все 7 типов тетрамино, систему очков, таблицу лидеров и несколько режимов игры.
//!
//! ## 🎮 Быстрый старт
//!
//! ### Пример запуска игры
//!
//! ```no_run
//! use tetris_cli::app;
//!
//! fn main() -> Result<(), Box<dyn std::error::Error>> {
//!     app::run();
//!     Ok(())
//! }
//! ```
//!
//! ### Пример работы с таблицей лидеров
//!
//! ```ignore
//! use tetris_cli::highscore::Leaderboard;
//!
//! // Загрузка таблицы лидеров
//! let mut leaderboard = Leaderboard::load();
//!
//! // Добавление рекорда
//! if leaderboard.add_score("Player", 1000) {
//!     leaderboard.save();
//! }
//!
//! // Получение лучшего рекорда
//! if let Some(entry) = leaderboard.get_entries().first() {
//!     println!("Лучший рекорд: {}", entry.score().unwrap_or(0));
//! }
//! ```
//!
//! ### Пример настройки управления
//!
//! ```ignore
//! use tetris_cli::controls::ControlsConfig;
//!
//! // Создание кастомной конфигурации
//! let config = ControlsConfig::custom(
//!     b'a', b'd', b's', b'w',  // движение
//!     b'q', b'e', b'c', b'p', b'\x08', // вращение, hold, pause, quit
//! );
//!
//! // Валидация конфигурации
//! if config.validate() {
//!     config.save_to_file("my_controls.json")?;
//! }
//! ```

//! ## 🎮 Основные возможности
//!
//! - **Классический геймплей**: Все 7 типов тетрамино (T, L, J, S, Z, O, I)
//! - **Плавная анимация**: Поддержка 60 FPS для плавного падения фигур
//! - **Прогрессивная сложность**: Увеличение скорости каждые 10 линий
//! - **Система уровней**: Повышение уровня с бонусными очками
//! - **Предпросмотр**: Показ следующей фигуры для стратегического планирования
//! - **Призрачная фигура**: Отображение точки приземления текущей фигуры
//! - **Удержание фигуры (Hold)**: Возможность сохранить фигуру на потом
//! - **Таблица лидеров**: Топ-5 лучших результатов с защитой от подделки
//! - **Режим спринт**: Быстрая игра на очистку 40 линий
//! - **Расширенная статистика**: Подсчёт фигур, комбо и времени игры
//! - **Звуковые эффекты**: Терминальный bell при удалении линий
//! - **Анимации**: Мигание фигуры при Hard Drop, анимация очистки линий
//!
//! ## 📦 Структура модулей
//!
//! Проект организован в виде набора модулей, каждый из которых отвечает за
//! определённую часть функциональности:
//!
//! ### [`game`] — Основной игровой цикл и логика
//!
//! Модуль содержит всю игровую логику:
//! - Структура `GameState` — состояние игры (счёт, уровень, фигуры, поле)
//! - Структура `GameStats` — статистика игры (фигуры, комбо, время)
//! - Перечисление `GameMode` — режимы игры (Классика/Спринт)
//! - Перечисление `Dir` — направления движения и вращения
//! - Константы игры (FPS, скорость, очки, бонусы)
//! - Игровой цикл `play()` — основной цикл отрисовки и обновления
//! - Методы обновления `update()` — обработка ввода и физика
//! - Методы отрисовки `draw()` — рендеринг игрового поля
//! - Система очков — начисление баллов за действия
//!
//! ### [`tetromino`] — Типы фигур и их поведение
//!
//! Модуль определяет все аспекты тетрамино:
//! - Перечисление `ShapeType` — 7 типов фигур (T, L, J, S, Z, O, I)
//! - Структура `Tetromino` — падающая фигура с координатами
//! - Структура `BagGenerator` — генератор фигур по системе 7-bag
//! - Константа `SHAPE_COORDS` — координаты блоков для каждой фигуры
//! - Константа `SHAPE_COLORS` — цвета для отрисовки фигур
//! - Метод `rotate()` — вращение фигур по часовой/против часовой
//! - Система 7-bag — гарантия равномерного распределения фигур
//! - Алгоритм Fisher-Yates — честное перемешивание мешка
//!
//! ### [`io`] — Ввод с клавиатуры и отрисовка
//!
//! Модуль предоставляет абстракции для работы с терминалом:
//! - Структура `Canvas` — канвас для отрисовки в терминале
//! - Структура `KeyReader` — асинхронный читатель нажатий клавиш
//! - Константы размеров (`GRID_WIDTH`, `GRID_HEIGHT`, `DISP_WIDTH`, `DISP_HEIGHT`)
//! - Методы отрисовки текста и графики
//! - Управление курсором и цветами терминала
//! - Raw-режим терминала для мгновенного чтения ввода
//!
//! ### [`highscore`] — Сохранение и загрузка рекордов
//!
//! Модуль системы рекордов и таблицы лидеров:
//! - Структура `SaveData` — хранение одиночного рекорда
//! - Структура `LeaderboardEntry` — запись в таблице лидеров
//! - Структура `Leaderboard` — таблица лидеров (топ-5)
//! - Структура `ControlsConfig` — конфигурация управления
//! - Хэширование с солью — защита рекордов от подделки
//! - Сериализация в TOML/JSON — сохранение в файл конфигурации
//!
//! ### [`controls`] — Конфигурация управления
//!
//! Модуль настройки клавиш управления:
//! - Структура `ControlsConfig` — все клавиши управления
//! - Значения по умолчанию — стандартная раскладка WASD/QE
//! - Валидация конфигурации — проверка на дубликаты клавиш
//! - Сохранение/загрузка — персистентность настроек
//! - Кастомизация — возможность изменить любую клавишу
//!
//! ### [`crypto`] — Криптографические утилиты
//!
//! Модуль предоставляет общие криптографические функции:
//! - `hash()` — хеширование BLAKE3
//! - `generate_salt()` — генерация случайной соли
//! - `hmac_sha256()` — HMAC-SHA256 подпись (криптографически стойкая)
//! - `verify_hmac_sha256()` — проверка HMAC подписи
//!
//! ## 🚀 Быстрый старт
//!
//! Для запуска игры используйте команду `tetris-cli` или импортируйте модули:
//! - [`game::GameState`] — основное состояние игры
//! - [`io::Canvas`] и [`io::KeyReader`] — ввод/вывод
//! - [`highscore::Leaderboard`] — таблица лидеров
//! - [`controls::ControlsConfig`] — конфигурация управления
//!
//! ### Пример работы с таблицей лидеров
//!
//! Используйте [`highscore::Leaderboard`] для работы с рекордами:
//! - `Leaderboard::load()` — загрузка таблицы лидеров
//! - `leaderboard.add_score()` — добавление рекорда
//! - `leaderboard.save()` — сохранение таблицы
//!
//! ### Пример настройки управления
//!
//! Используйте [`controls::ControlsConfig`] для настройки клавиш:
//! - `ControlsConfig::custom()` — создание конфигурации с произвольными клавишами
//! - `config.validate()` — проверка конфигурации на дубликаты
//! - `config.save_to_file()` — сохранение в файл
//!
//! ## 🎯 Система очков
//!
//! Игра использует классическую систему начисления очков:
//!
//! ### Базовые очки
//!
//! | Действие | Формула | Пример |
//! |----------|---------|--------|
//! | Фигура | 100 + (скорость × 50) | ~145 очков |
//! | Soft Drop | 1 × ячейки | 5 ячеек = 5 очков |
//! | Hard Drop | 2 × ячейки | 10 ячеек = 20 очков |
//!
//! ### Очки за линии (с экспоненциальным бонусом)
//!
//! | Линий | Формула | Очки | Название |
//! |-------|---------|------|----------|
//! | 1 | 100 × 2⁰ | 100 | Одиночный |
//! | 2 | 100 × 2¹ | 200 | Двойной |
//! | 3 | 100 × 2² | 400 | Тройной |
//! | 4 | 100 × 2³ + 1000 | 1800 | **TETRIS!** |
//!
//! ### Бонусы
//!
//! | Бонус | Условие | Очки |
//! |-------|---------|------|
//! | Комбо | 50 × (комбо - 1) | до 450+ |
//! | Уровень | 500 × (уровень - 1) | до 5000+ |
//! | Tetris | 4 линии одновременно | +1000 |
//!
//! ## 🎮 Управление
//!
//! ### Клавиши по умолчанию
//!
//! | Клавиша | Действие | Описание |
//! |---------|----------|----------|
//! | `A` | Влево | Перемещение фигуры влево |
//! | `D` | Вправо | Перемещение фигуры вправо |
//! | `Q` | Вращение ↺ | Поворот против часовой стрелки |
//! | `E` | Вращение ↻ | Поворот по часовой стрелке |
//! | `W` | Hard Drop | Мгновенное падение (2 очка/ячейку) |
//! | `S` | Soft Drop | Ускоренное падение (1 очко/ячейку) |
//! | `C` | Hold | Удержать фигуру на потом |
//! | `P` | Pause | Пауза игры |
//! | `Backspace` | Выход | Вернуться в меню |
//!
//! ### Настройка управления
//!
//! Все клавиши можно перенастроить через `ControlsConfig`:
//!
//! ```ignore
//! use tetris_cli::controls::ControlsConfig;
//!
//! // Загрузка сохранённой конфигурации
//! let config = ControlsConfig::load_from_file("my_controls.json").unwrap();
//!
//! // Или создание новой
//! let custom = ControlsConfig::custom(
//!     b'4', b'6', b'5', b'8',  // Стрелки (цифровая клавиатура)
//!     b'1', b'3', b'0', b'9', b'7',
//! );
//! ```
//!
//! ## 📊 Режимы игры
//!
//! ### Классический режим
//!
//! - **Цель**: Набрать как можно больше очков
//! - **Конец игры**: При заполнении поля до верха
//! - **Сохранение**: Рекорд сохраняется в таблицу лидеров
//! - **Прогрессия**: Скорость растёт каждые 10 линий
//!
//! ### Режим спринт
//!
//! - **Цель**: Очистить 40 линий как можно быстрее
//! - **Конец игры**: При достижении 40 линий
//! - **Сохранение**: Только статистика, без рекордов
//! - **Таймер**: Отображается время прохождения
//!
//! ## 🔧 Системные требования
//!
//! ### Минимальные требования
//!
//! - **ОС**: Linux или Unix-подобная система
//! - **Терминал**: С поддержкой ANSI-цветов и raw-режима
//! - **Размер окна**: Минимум 22×25 символов
//! - **Шрифт**: Моноширинный (рекомендуется)
//!
//! ### Рекомендуемые терминалы
//!
//! - GNOME Terminal
//! - Konsole
//! - Alacritty
//! - Kitty
//! - iTerm2 (macOS)
//!
//! ## 🏗️ Архитектура
//!
//! Подробная документация по архитектуре проекта доступна в файле [`ARCHITECTURE.md`](../ARCHITECTURE.md).
//!
//! ### Игровой цикл
//!
//! ```text
//! ┌─────────────────┐
//! │   Инициализация │
//! │   - Загрузка    │
//! │   - Настройки   │
//! └────────┬────────┘
//!          │
//!          ▼
//! ┌─────────────────┐
//! │  Игровой цикл   │
//! │  (60 FPS)       │
//! └────────┬────────┘
//!          │
//!    ┌─────┴─────┐
//!    │           │
//!    ▼           ▼
//! ┌──────┐   ┌──────┐
//! │Update│   │ Draw │
//! │- Ввод│   │- Поле│
//! │- Физика│  │- UI  │
//! │- Логика│  │- HUD │
//! └──────┘   └──────┘
//! ```
//!
//! ### Система 7-bag
//!
//! ```text
//! Мешок 1: [T, L, J, S, Z, O, I] → перемешать → выдавать по очереди
//! Мешок 2: [I, O, Z, S, J, L, T] → перемешать → выдавать по очереди
//! ...
//! ```
//!
//! Гарантирует, что каждые 7 фигур будут содержать все 7 типов,
//! что делает игру более предсказуемой и справедливой.
