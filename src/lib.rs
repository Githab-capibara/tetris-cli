//! # Tetris CLI
//!
//! Классическая игра Тетрис для терминала Linux/Unix.
//!
//! ## Обзор
//!
//! Этот проект реализует полноценную игру Тетрис с использованием терминального интерфейса.
//! Поддерживаются все классические механики: вращение фигур, мгновенное падение,
//! подсчёт очков и сохранение рекордов.
//!
//! ## Модули
//!
//! - [`game`] — основной игровой цикл и логика
//! - [`tetromino`] — типы фигур и их поведение
//! - [`io`] — ввод с клавиатуры и отрисовка
//! - [`highscore`] — сохранение и загрузка рекордов
//!
//! ## Пример использования
//!
//! ```no_run
//! use tetris_cli::game::GameState;
//! use tetris_cli::io::{Canvas, KeyReader};
//!
//! // Создание нового состояния игры
//! let mut state = GameState::new();
//!
//! // Инициализация канваса и читателя клавиш
//! let mut cnv = Canvas::new();
//! let mut inp = KeyReader::new();
//!
//! // Запуск игры (возвращает финальный счёт)
//! let score = state.play(&mut cnv, &mut inp, &String::from("0"));
//! ```
//!
//! ## Системные требования
//!
//! - ОС: Linux или Unix-подобная система
//! - Терминал: с поддержкой raw-режима и ANSI-цветов
//! - Минимальный размер окна: 22×25 символов
//!
//! ## Лицензия
//!
//! GPL-3.0

pub mod game;
pub mod highscore;
pub mod io;
pub mod tetromino;
