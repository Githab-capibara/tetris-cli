//! Модуль конфигурации приложения Tetris CLI.
//!
//! # Обзор
//!
//! Этот модуль предоставляет централизованное управление конфигурацией приложения,
//! включая HMAC ключи для защиты целостности данных.
//!
//! # Подмодули
//!
//! - [`keys`] — управление HMAC ключами для подписи конфигурационных файлов,
//!   таблицы лидеров и сохранённых данных
//!
//! # Безопасность
//!
//! Все конфигурационные файлы защищены HMAC-SHA256 подписями для предотвращения
//! несанкционированной модификации. HMAC ключи загружаются из переменных окружения
//! при первом обращении и кэшируются для производительности.
//!
//! # Использование
//!
//! ```ignore
//! use tetris_cli::config::keys::{get_controls_hmac_key, validate_all_keys};
//!
//! // Валидация всех ключей при запуске приложения
//! validate_all_keys()?;
//!
//! // Получение ключа для подписи конфигурации управления
//! let key = get_controls_hmac_key();
//! ```
//!
