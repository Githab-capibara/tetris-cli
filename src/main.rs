/// Точка входа в приложение Tetris CLI.
///
/// ## Выполняемые действия:
///
/// 1. **Инициализация данных**:
///    - Загрузка рекорда из файла конфигурации (`SaveData::load_config()`)
///    - Загрузка и валидация таблицы лидеров (`Leaderboard::load()`)
///
/// 2. **Проверка терминала**:
///    - Получение текущего размера терминала
///    - Проверка на соответствие минимальным требованиям (22×25)
///    - Вывод ошибки и завершение при недостаточном размере
///
/// 3. **Инициализация ввода/вывода**:
///    - Создание Canvas для отрисовки в терминале
///    - Создание `KeyReader` для чтения нажатий клавиш
///
/// 4. **Главный цикл меню** (60 FPS):
///    - Отрисовка меню с текущим рекордом
///    - Поддержание стабильного FPS через `sleep()`
///    - Обработка нажатий клавиш:
///      - `Enter` — запуск классической игры
///      - `R` — запуск режима спринт
///      - `L` — отображение таблицы лидеров
///      - `Backspace` (127) — выход из приложения
///
/// 5. **После завершения игры**:
///    - Отображение статистики игры
///    - Запрос имени для таблицы лидеров (если новый рекорд)
///    - Сохранение рекорда в таблицу и файл конфигурации
///
/// ## 🎯 Пример потока выполнения:
///
/// ```text
/// main()
///   ├─ Загрузка SaveData (рекорд)
///   ├─ Загрузка Leaderboard (таблица лидеров)
///   ├─ Проверка размера терминала
///   │   └─ Если < 22×25 → ошибка и выход
///   ├─ Создание Canvas и KeyReader
///   └─ Главный цикл меню:
///       ├─ Отрисовка MENU + рекорд
///       ├─ Ожидание ввода (60 FPS)
///       ├─ Обработка клавиш:
///       │   ├─ Enter → play(Classic) → show_stats → save_score
///       │   ├─ 'r' → play(Sprint) → show_stats
///       │   ├─ 'l' → show_leaderboard()
///       │   └─ Backspace → выход
///       └─ Повтор цикла
/// ```
///
/// ## ⚠️ Обработка ошибок
///
/// Функция возвращает `Result<(), Box<dyn std::error::Error>>` для корректной обработки ошибок:
/// - Ошибка размера терминала → возвращает `Err`
/// - Ошибка инициализации терминала → возвращает `Err`
/// - Ошибка загрузки данных → логируется и используется значение по умолчанию
///
/// Критические ошибки (терминал, ввод/вывод) приводят к завершению программы с кодом ошибки.
/// Некритические ошибки (загрузка данных) логируются, но не прерывают выполнение.
///
/// ## 📝 Примечания
///
/// - Цикл меню работает на 60 FPS для плавной отрисовки
/// - Поддерживает FPS через расчёт дельты времени и `sleep()`
/// - Рекорд отображается в реальном времени в меню
/// - Выход через Backspace корректно сбрасывает терминал
///
/// # Архитектурные заметки
///
/// TODO (#архитектура): Выделить в отдельные функции:
/// - `load_game_data()` - загрузка сохранённых данных и таблицы лидеров
/// - `initialize_terminal()` - проверка терминала и инициализация Canvas/KeyReader
/// - `run_menu_loop()` - главный цикл меню с обработкой ввода
/// - `handle_game_completion()` - обработка завершения игры и сохранение рекорда
fn main() -> Result<(), Box<dyn std::error::Error>> {
    // ========================================================================
    // ШАГ 1: ЗАГРУЗКА СОХРАНЁННЫХ ДАННЫХ
    // ========================================================================
    // TODO (#архитектура): Выделить в функцию load_game_data()

    // Загрузка данных конфигурации из файла
    // SaveData содержит:
    // - high_score: значение рекорда
    // - high_score_salt: случайная соль для хэша
    // - high_score_hash: хэш для защиты от подделки
    let save = SaveData::load_config();

    // Проверка целостности рекорда и получение значения
    // verify_and_get_score() возвращает Some(score) если хэш совпадает, None при подделке
    let mut high_score = save.verify_and_get_score().unwrap_or_else(|| {
        // Логирование попытки подделки рекорда
        eprintln!("Предупреждение: обнаружена попытка подделки рекорда! Используется значение 0.");
        0
    });

    // ========================================================================
    // ШАГ 2: ЗАГРУЗКА ТАБЛИЦЫ ЛИДЕРОВ
    // ========================================================================
    // TODO (#архитектура): Выделить в функцию load_game_data()

    // Загрузка таблицы лидеров из файла конфигурации
    // Leaderboard содержит до 5 записей (топ-5 результатов)
    let mut leaderboard = Leaderboard::load();

    // Валидация всех записей в таблице лидеров
    // validate() удаляет записи с невалидным хэшем (подделанные)
    leaderboard.validate();

    // ========================================================================
    // ШАГ 3: ПРОВЕРКА РАЗМЕРА ТЕРМИНАЛА
    // ========================================================================
    // TODO (#архитектура): Выделить в функцию initialize_terminal()

    // Получение текущего размера терминала (ширина, высота) в символах
    // terminal_size() возвращает Result<(u16, u16), Error>
    let (width, height) = terminal_size().map_err(|e| {
        eprintln!(
            "Ошибка: не удалось получить размер терминала: {e}.\n\
             Минимальный размер: {DISP_WIDTH}x{DISP_HEIGHT} символов."
        );
        e
    })?;

    // Проверка на соответствие минимальным требованиям
    // DISP_WIDTH = 22 символа (ширина игрового поля + границы)
    // DISP_HEIGHT = 25 строк (высота поля + заголовки + меню)
    if width < DISP_WIDTH || height < DISP_HEIGHT {
        // Вывод подробного сообщения об ошибке в stderr
        let err = std::io::Error::new(
            std::io::ErrorKind::InvalidInput,
            format!(
                "Окно терминала слишком маленькое!\n\
                 Минимальный размер: {DISP_WIDTH}x{DISP_HEIGHT} символов.\n\
                 Текущий размер: {width}x{height} символов."
            ),
        );
        // Исправление #15: используем {err} вместо "{}", err
        eprintln!("{err}");
        return Err(err.into());
    }

    // ========================================================================
    // ШАГ 4: ИНИЦИАЛИЗАЦИЯ ВВОДА/ВЫВОДА
    // ========================================================================
    // TODO (#архитектура): Выделить в функцию initialize_terminal()

    // Создание канваса для отрисовки в терминале
    // Canvas::new() автоматически:
    // - Переводит терминал в raw-режим
    // - Очищает экран
    // - Скрывает курсор
    // Возвращает Result для корректной обработки ошибок
    let mut cnv = Canvas::new().map_err(|e| {
        // Исправление #15: используем {e} вместо "{}", e
        eprintln!("Ошибка инициализации терминала: {e}");
        e
    })?;

    // Создание читателя нажатий клавиш
    // KeyReader::new() инициализирует async_stdin для неблокирующего чтения
    let mut inp = KeyReader::new();

    // ========================================================================
    // ШАГ 5: ГЛАВНЫЙ ЦИКЛ МЕНЮ
    // ========================================================================
    // TODO (#архитектура): Выделить в функцию run_menu_loop()

    // Переменная для отслеживания времени последнего кадра
    // Используется для поддержания стабильного FPS
    let mut last_time = Instant::now();

    // Интервал между кадрами в миллисекундах
    // При FPS = 60, interval_ms = 1000 / 60 ≈ 16.67 мс
    let interval_ms = 1_000 / FPS;

    // Бесконечный цикл меню (работает до нажатия Backspace)
    loop {
        // ---------------------------------------------------------------
        // ПОДДЕРЖАНИЕ СТАБИЛЬНОГО FPS
        // ---------------------------------------------------------------

        // Получение текущего времени
        let now = Instant::now();

        // Расчёт времени, прошедшего с последнего кадра (в миллисекундах)
        // subsec_millis() возвращает дробную часть секунды как u32
        let delta_time_ms = u64::from(now.duration_since(last_time).subsec_millis());

        // Если прошло меньше времени чем нужно для целевого FPS
        if delta_time_ms < interval_ms {
            // Вычисляем необходимое время сна для поддержания FPS
            // Например: если прошло 10 мс вместо 16.67 мс, спим 6.67 мс
            sleep(Duration::from_millis(interval_ms - delta_time_ms));
            // Переходим к следующей итерации цикла (пропускаем отрисовку)
            continue;
        }

        // Обновляем время последнего кадра
        last_time = now;

        // ---------------------------------------------------------------
        // ПОДГОТОВКА ДАННЫХ ДЛЯ ОТРИСОВКИ
        // ---------------------------------------------------------------

        // Преобразование рекорда в строку для отображения в меню
        // {:10} означает форматирование с шириной 10 символов (выравнивание)
        let high_score_display = format!("{high_score:10}");

        // ---------------------------------------------------------------
        // ОТРИСОВКА МЕНЮ
        // ---------------------------------------------------------------

        // Отрисовка статических строк меню
        // draw_strs() рисует массив строк начиная с позиции (1, 1)
        // MENU_COLOR = White, Reset = сброс цвета фона
        cnv.draw_strs(&MENU, (1, 1), &MENU_COLOR, &Reset);

        // Отрисовка текущего рекорда на позиции (11, 21)
        // Позиция выбрана так, чтобы попасть в поле "Рекорд:" в меню
        cnv.draw_string(&high_score_display, (11, 21), &MENU_COLOR, &Reset);

        // Обновление экрана (flush буфера терминала)
        // Без этого изменения не появятся на экране
        cnv.flush();

        // ---------------------------------------------------------------
        // ОБРАБОТКА ВВОДА ПОЛЬЗОВАТЕЛЯ
        // ---------------------------------------------------------------

        // Получение кода нажатой клавиши (блокирующий вызов)
        // get_key() возвращает Some(u8) код клавиши или None при ошибке
        let key = inp.get_key();

        // Обработка нажатой клавиши через match
        match key {
            // ===========================================================
            // ЗАПУСК КЛАССИЧЕСКОЙ ИГРЫ (Enter)
            // ===========================================================
            Some(b'\n' | b'\r') => {
                // Создаём новое состояние игры для классического режима
                let state = GameState::new();

                // Запуск игрового режима с сохранением в таблицу лидеров
                let new_score = run_game_mode(
                    &mut cnv,
                    &mut inp,
                    &high_score_display,
                    state,
                    true, // Сохранять в таблицу лидеров
                    &mut leaderboard,
                );

                // Сохранение лучшего рекорда
                if new_score > high_score {
                    high_score = new_score;
                    SaveData::save_value(high_score);
                }
            }

            // ===========================================================
            // ЗАПУСК РЕЖИМА СПРИНТ (R)
            // ===========================================================
            Some(b'r') => {
                // Создаём состояние игры для режима спринт
                let state = GameState::new_sprint();

                // Запуск игрового режима без сохранения в таблицу лидеров
                run_game_mode(
                    &mut cnv,
                    &mut inp,
                    &high_score_display,
                    state,
                    false, // Не сохранять в таблицу лидеров
                    &mut leaderboard,
                );
            }

            // ===========================================================
            // ЗАПУСК РЕЖИМА МАРАФОН (M)
            // ===========================================================
            Some(b'm') => {
                // Создаём состояние игры для режима марафон
                let state = GameState::new_marathon();

                // Запуск игрового режима с сохранением в таблицу лидеров
                let new_score = run_game_mode(
                    &mut cnv,
                    &mut inp,
                    &high_score_display,
                    state,
                    true, // Сохранять в таблицу лидеров
                    &mut leaderboard,
                );

                // Сохранение лучшего рекорда
                if new_score > high_score {
                    high_score = new_score;
                    SaveData::save_value(high_score);
                }
            }

            // ===========================================================
            // ОТОБРАЖЕНИЕ ТАБЛИЦЫ ЛИДЕРОВ (L)
            // ===========================================================
            Some(b'l') => {
                // Вызов функции отображения таблицы лидеров
                // show_leaderboard() рисует топ-5 рекордов и ждёт клавишу
                show_leaderboard(&mut cnv, &mut inp, &leaderboard);
            }

            // ===========================================================
            // ВЫХОД ИЗ ПРИЛОЖЕНИЯ (Backspace)
            // ===========================================================
            Some(KEY_BACKSPACE) => {
                // Клавиша Backspace — прерывание главного цикла меню
                break;
            }

            // ===========================================================
            // НЕИЗВЕСТНАЯ КЛАВИША
            // ===========================================================
            Some(_) | None => {
                // Игнорирование всех остальных клавиш
                // Цикл продолжается, меню перерисовывается
            }
        }
    }

    // ========================================================================
    // ШАГ 6: ЗАВЕРШЕНИЕ РАБОТЫ
    // ========================================================================

    // Сброс терминала в исходное состояние
    // reset() автоматически:
    // - Показывает курсор (Show)
    // - Выполняет flush буфера
    // - Возвращает терминал в нормальный режим
    cnv.reset();

    Ok(())
}

