//! # Точка входа в игру Tetris CLI
//!
//! ## 📖 Описание модуля
//!
//! Этот файл содержит точку входа в приложение (`main()`).
//! Вся логика приложения выделена в модуль [`crate::app`].
//!
//! ## 🏗️ Архитектура
//!
//! ```text
//! ┌─────────────────┐
//! │     main()      │
//! │   Точка входа   │
//! └────────┬────────┘
//!          │
//!          ▼
//! ┌─────────────────┐
//! │   app::run()    │
//! │  Application    │
//! └─────────────────┘
//! ```
//!
//! ## 📦 Модули
//!
//! Файл импортирует следующие внутренние модули:
//! - `app` — приложение (Application layer)
//! - `crypto` — криптографические утилиты
//! - `types` — общие типы
//! - `game` — игровая логика
//! - `io` — ввод/вывод
//! - `io_traits` — трейты ввода/вывода
//! - `menu` — главное меню
//! - `tetromino` — фигуры тетрамино

// ============================================================================
// ИМПОРТЫ И ЗАВИСИМОСТИ
// ============================================================================

