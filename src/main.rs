/// Показать статистику после завершения игры.
///
/// ## Назначение
///
/// Эта функция отображает подробную статистику прошедшей игры
/// в виде красивого интерфейса с рамкой. Вызывается автоматически
/// после завершения игры (проигрыш или выход).
///
/// ## 🎯 Пример использования
///
/// ```no_run
/// use tetris_cli::game::GameState;
/// use tetris_cli::io::{Canvas, KeyReader};
///
/// let mut cnv = Canvas::new();
/// let mut inp = KeyReader::new();
/// let state = GameState::new();
///
/// // После завершения игры показать статистику
/// // show_game_stats(&mut cnv, &mut inp, &state);
/// ```
///
/// ## 📋 Аргументы
///
/// | Аргумент | Тип | Описание |
/// |----------|-----|----------|
/// | `cnv` | `&mut Canvas` |Mutable ссылка на канвас для отрисовки |
/// | `inp` | `&mut KeyReader` |Mutable ссылка на читатель нажатий клавиш |
/// | `state` | `&GameState` |Неизменяемая ссылка на состояние игры |
///
/// ## 📊 Отображаемая статистика
///
/// | Поле | Описание | Пример |
/// |------|----------|--------|
/// | Режим | Тип игры (Классика/Спринт) | "Классика" |
/// | Время | Продолжительность игры | "125.3 сек" |
/// | Фигур | Общее количество фигур | "47" |
/// | T, L, J | Фигуры по типам (группа 1) | "T: 8 L: 6 J: 7" |
/// | S, Z, O | Фигуры по типам (группа 2) | "S: 5 Z: 4 O: 9" |
/// | I | Фигуры типа I (отдельно) | "I: 8" |
/// | Макс. комбо | Лучшее комбо за игру | "4" |
///
/// ## 🎨 Визуальный интерфейс
///
/// ```text
/// ╔════════════════════╗
/// ║   СТАТИСТИКА ИГРЫ  ║
/// ║                    ║
/// ║ Режим: Классика    ║
/// ║ Время: 125.3       ║
/// ║ Фигур: 47          ║
/// ║ T: 8 L: 6 J: 7     ║
/// ║ S: 5 Z: 4 O: 9     ║
/// ║ I: 8               ║
/// ║ Макс. комбо: 4     ║
/// ║                    ║
/// ║  Любая клавиша...  ║
/// ╚════════════════════╝
/// ```
///
/// ## 🔧 Технические детали
///
/// ### Получение данных:
/// - `state.get_stats()` — получение `GameStats`
/// - `state.get_mode()` — определение режима игры
/// - `stats.get_elapsed_time()` — время в секундах (f64)
/// - `stats.total_pieces()` — сумма всех фигур
/// - `stats.max_combo` — лучшее комбо
///
/// ### Форматирование:
/// - Все числа выравниваются для единообразия
/// - Время отображается с 1 знаком после запятой
/// - Фигуры группируются по 3 для компактности
///
/// ## ⏱️ Производительность
///
/// Использует `sleep(Duration::from_millis(16))` для предотвращения
/// высокой нагрузки на CPU во время ожидания ввода.
fn show_game_stats(cnv: &mut Canvas, inp: &mut KeyReader, state: &GameState) {
    // ========================================================================
    // ПОЛУЧЕНИЕ СТАТИСТИКИ ИЗ СОСТОЯНИЯ ИГРЫ
    // ========================================================================

    // Получение структуры статистики из состояния игры
    // GameStats содержит:
    // - t_pieces, l_pieces, j_pieces, s_pieces, z_pieces, o_pieces, i_pieces
    // - max_combo, combo_counter
    // - start_time, end_time (для расчёта времени)
    let stats = state.get_stats();

    // Формирование массива строк для отрисовки
    // Статические строки (рамка) и динамические (данные статистики)
    let lines = [
        "╔════════════════════╗", // Верхняя граница
        "║   СТАТИСТИКА ИГРЫ  ║", // Заголовок
        "║                    ║", // Пустая строка (отступ)
        // Режим игры
        &match state.get_mode() {
            GameMode::Classic => format!("║ Режим: {:14} ║", "Классика"),
            GameMode::Sprint => format!("║ Режим: {:14} ║", "Спринт"),
            GameMode::Marathon => format!("║ Режим: {:14} ║", "Марафон"),
        },
        &format!("║ Время: {:14.1} ║", stats.get_elapsed_time()), // Время с 1 знаком после запятой
        &format!("║ Фигур: {:14} ║", stats.total_pieces()),       // Общее количество фигур
        // Группа фигур 1: T, L, J (по 3 символа на тип)
        &format!(
            "║ T: {:3} L: {:3} J: {:3}  ║",
            stats.t_pieces, stats.l_pieces, stats.j_pieces
        ),
        // Группа фигур 2: S, Z, O
        &format!(
            "║ S: {:3} Z: {:3} O: {:3}  ║",
            stats.s_pieces, stats.z_pieces, stats.o_pieces
        ),
        // Фигура типа I (отдельно, важна для Tetris)
        &format!("║ I: {:3}              ║", stats.i_pieces),
        &format!("║ Макс. комбо: {:10} ║", stats.max_combo), // Максимальное комбо
        "║                    ║",                            // Пустая строка (отступ)
        "║  Любая клавиша...  ║",                            // Подсказка пользователю
        "╚════════════════════╝",                            // Нижняя граница
    ];

    // ========================================================================
    // ОТРИСОВКА СТАТИСТИКИ
    // ========================================================================

    // Отрисовка всех строк статистики начиная с позиции (1, 1)
    // draw_strs() автоматически увеличивает Y для каждой строки
    cnv.draw_strs(&lines, (1, 1), MENU_COLOR, &Reset);

    // Обновление экрана (применение изменений)
    cnv.flush();

    // ========================================================================
    // ОЖИДАНИЕ НАЖАТИЯ КЛАВИШИ
    // ========================================================================

    // Бесконечный цикл ожидания ввода от пользователя
    // Завершается при нажатии любой клавиши
    loop {
        // Получение кода нажатой клавиши
        let key = inp.get_key();

        // Проверка: была ли нажата какая-либо клавиша
        // Some(_) означает успешное чтение клавиши
        if key.is_some() {
            // Прерывание цикла и возврат в главное меню
            break;
        }

        // Задержка для предотвращения высокой нагрузки на CPU
        // 16 мс = ~60 опросов в секунду — достаточно для отзывчивости
        // Используется общая константа FRAME_DELAY_MS из lib.rs
        sleep(Duration::from_millis(FRAME_DELAY_MS));
    }
    // После нажатия клавиши функция завершается, управление возвращается в main()
}
