//! # Точка входа в игру Tetris CLI
//!
//! ## 📖 Описание модуля
//!
//! Этот файл содержит точку входа в приложение (`main()`) и реализует:
//! - Главное меню с отображением управления и информации об игре
//! - Загрузку и сохранение рекордов из файла конфигурации
//! - Проверку минимального размера терминала
//! - Запуск игрового цикла после выбора режима
//! - Выбор режима игры (классический или спринт)
//! - Отображение таблицы лидеров
//! - Показ статистики после завершения игры
//! - Ввод имени игрока для таблицы лидеров
//!
//! ## 🏗️ Архитектура
//!
//! ```text
//! ┌─────────────────┐
//! │     main()      │
//! │   Точка входа   │
//! └────────┬────────┘
//!          │
//!    ┌─────┴──────┐
//!    │            │
//!    ▼            ▼
//! ┌─────────┐  ┌──────────┐
//! │Загрузка │  │ Проверка │
//! │рекордов │  │ терминала│
//! └────┬────┘  └─────┬────┘
//!      │            │
//!      └─────┬──────┘
//!            │
//!            ▼
//!    ┌───────────────┐
//!    │ Главный цикл  │
//!    │    меню       │
//!    └───────┬───────┘
//!            │
//!    ┌───────┴────────┐
//!    │                │
//!    ▼                ▼
//! ┌─────────┐    ┌──────────┐
//! │  Игра   │    │Таблица   │
//! │(Enter/r)│    │лидеров(l)│
//! └─────────┘    └──────────┘
//! ```
//!
//! ## 📦 Модули
//!
//! Файл импортирует и использует следующие внутренние модули:
//! - `game` — игровая логика и состояние
//! - `highscore` — система рекордов и таблицы лидеров
//! - `io` — ввод/вывод в терминале
//! - `tetromino` — фигуры тетрамино (не используется напрямую, но требуется)
//!
//! ## 🎯 Примеры использования функций
//!
//! ### Запрос имени игрока
//!
//! ```no_run
//! use tetris_cli::io::{Canvas, KeyReader};
//! use termion::color::{White, Reset};
//!
//! let mut cnv = Canvas::new();
//! let mut inp = KeyReader::new();
//!
//! // Функция запрашивает имя и возвращает строку
//! let name = get_player_name(&mut cnv, &mut inp);
//! // Игрок вводит имя, нажимает Enter для подтверждения
//! ```
//!
//! ### Отображение таблицы лидеров
//!
//! ```no_run
//! use tetris_cli::highscore::Leaderboard;
//! use tetris_cli::io::{Canvas, KeyReader};
//!
//! let mut cnv = Canvas::new();
//! let mut inp = KeyReader::new();
//! let leaderboard = Leaderboard::load();
//!
//! // Отображает топ-5 рекордов
//! // show_leaderboard(&mut cnv, &mut inp, &leaderboard);
//! ```
//!
//! ### Показ статистики игры
//!
//! ```no_run
//! use tetris_cli::game::GameState;
//! use tetris_cli::io::{Canvas, KeyReader};
//!
//! let mut cnv = Canvas::new();
//! let mut inp = KeyReader::new();
//! let state = GameState::new();
//!
//! // Показывает статистику после игры
//! // show_game_stats(&mut cnv, &mut inp, &state);
//! ```

