//! - Загрузку и сохранение рекордов из файла конфигурации
//! - Проверку минимального размера терминала
//! - Запуск игрового цикла после выбора режима
//! - Выбор режима игры (классический или спринт)
//! - Отображение таблицы лидеров
//! - Показ статистики после завершения игры
