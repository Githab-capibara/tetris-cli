//! Модуль ввода/вывода для работы с терминалом.
//!
//! Этот модуль предоставляет абстракции для работы с терминалом:
//! - [`Canvas`] - канвас для отрисовки
//! - [`KeyReader`] - асинхронный читатель клавиатуры
//! - [`backend`] - абстракция и реализация терминального бэкенда
//!
//! ## Архитектурные заметки
//!
//! ### PROB-132: Терминальная зависимость
//! TODO: рассмотреть абстрагирование от termion для поддержки кроссплатформенности
//! (crossterm на Windows, pancurses для универсальной поддержки).
//!
//! ### PROB-135: Нет абстракции над терминалом
//! TODO: Canvas и KeyReader привязаны к termion напрямую. В идеале создать
//! trait TerminalBackend и реализовать для termion/crossterm/pancurses.
//! Сейчас это breaking change — оставить до следующего крупного релиза.
//!
//! Архитектурное улучшение 2026-04-01 (S3): Разделение io.rs на отдельные модули:
//! - `canvas.rs` - отрисовка в терминале
//! - `key_reader.rs` - чтение клавиатуры
//! - `backend.rs` - абстракция и реализация терминального бэкенда

// Подмодули
pub mod backend;
pub mod canvas;
// termion поддерживает только Unix — на Windows модуль недоступен
#[cfg(unix)]
pub mod key_reader;

// Re-export основных типов для обратной совместимости
pub use canvas::Canvas;
#[cfg(unix)]
pub use key_reader::KeyReader;
