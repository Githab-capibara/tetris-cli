//! Система сохранения рекордов.
//!
//! Этот модуль предоставляет функциональность для сохранения и загрузки рекордов игры.
//! Поддерживается как одиночный рекорд, так и таблица лидеров (топ-5 результатов).
//! Все рекорды защищены от подделки с помощью криптографического хеширования с солью.
//!
//! ## Безопасность
//!
//! В этом модуле используется криптографическая хеш-функция BLAKE3 для вычисления хешей.
//! **BLAKE3 является криптографически стойкой** и обеспечивает надёжную защиту от подделки рекордов.
//! BLAKE3 — современная быстрая хеш-функция, основанная на BLAKE2.
//! Для генерации соли используется криптографически стойкий генератор случайных чисел (getrandom).
//!
//! ## Структура модуля
//!
//! Модуль разделён на подмодули:
//! - `save_data` — хранение одиночного рекорда (`SaveData`)
//! - [`leaderboard`] — таблица лидеров (Leaderboard, `LeaderboardEntry`) - фасад
//! - [`leaderboard_repository`] — сериализация/персистентность (`LeaderboardRepository`)
//! - [`storage`] — хранилище и валидация записей (`LeaderboardStorage`, `LeaderboardValidator`)
//!
//! ## Архитектурное улучшение 2026-04-01 (CRITICAL #2)
//! Large Class leaderboard.rs разделён на подкомпоненты:
//! - `LeaderboardStorage` - хранение записей и управление коллекцией
//! - `LeaderboardValidator` - HMAC валидация и проверка целостности
//! - `LeaderboardRepository` - сериализация/десериализация и персистентность
//! - `Leaderboard` - фасад для обратной совместимости

