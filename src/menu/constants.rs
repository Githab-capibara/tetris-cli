/// Позиции элементов меню (X координаты).
///
/// # Обоснование значения (11)
/// Значение 11 выбрано как центрирование текста в рамке меню:
/// - Левая граница рамки: 2 символа ("║ ")
/// - Отступ для центрирования: 9 символов
/// - Итого: 2 + 9 = 11
///
/// Это обеспечивает визуальное центрирование заголовков внутри рамки 22 символа.
#[allow(dead_code)]
pub const MENU_TITLE_X: u16 = 11;

/// Позиция X для имени автора.
///
/// # Обоснование значения
/// Значение 11 выбрано аналогично [`MENU_TITLE_X`] для центрирования.
#[allow(dead_code)]
pub const MENU_AUTHOR_X: u16 = 11;

/// Позиция X для блока управления.
///
/// # Обоснование значения
/// Значение 11 выбрано аналогично [`MENU_TITLE_X`] для центрирования.
#[allow(dead_code)]
pub const MENU_CONTROLS_X: u16 = 11;

/// Позиция X для блока рекорда.
///
/// # Обоснование значения
/// Значение 11 выбрано аналогично [`MENU_TITLE_X`] для центрирования.
#[allow(dead_code)]
pub const MENU_RECORD_X: u16 = 11;

/// Позиции элементов меню (Y координаты).
///
/// # Обоснование значения (3)
/// Значение 3 выбрано как отступ от верха рамки:
/// - Строка 1: верхняя граница рамки
/// - Строка 2: пустая строка для отступа
/// - Строка 3: начало заголовка
///
/// Это обеспечивает визуальный отступ сверху для лучшего восприятия.
#[allow(dead_code)]
pub const MENU_TITLE_Y: u16 = 3;

/// Позиция Y для блока рекорда.
///
/// # Обоснование значения (21)
/// Значение 21 выбрано как размещение в нижней части меню:
/// - Строка 25: нижняя граница рамки
/// - Строки 22-24: блок рекорда
/// - Строка 21: заголовок рекорда
///
/// Это размещает рекорд под режимами игры для логической группировки.
#[allow(dead_code)]
pub const MENU_RECORD_Y: u16 = 21;

