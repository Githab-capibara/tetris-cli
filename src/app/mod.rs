//! Модуль приложения.
//!
//! Предоставляет структуру Application для управления жизненным циклом приложения.
//!
//! ## Архитектура
//!
//! ```text
//! ┌─────────────────┐
//! │  Application    │
//! │  ───────────    │
//! │  - canvas       │
//! │  - input        │
//! │  - leaderboard  │
//! │  - high_score   │
//! └────────┬────────┘
//!          │
//!    ┌─────┴──────┐
//!    │            │
//!    ▼            ▼
//! ┌─────────┐  ┌──────────┐
//! │  new()  │  │  run()   │
//! │  init   │  │  loop    │
//! └─────────┘  └──────────┘
//! ```
//!
//! ## Пример использования
//!
//! ```ignore
//! use crate::app;
//!
//! fn main() -> Result<(), Box<dyn std::error::Error>> {
//!     app::run()?;
//!     Ok(())
//! }
//! ```

mod application;

pub use application::{run, Application};
